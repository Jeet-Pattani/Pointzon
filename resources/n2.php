      <p>Disclosures made as part of a new government register of property owned via offshore entities show that China’s investment division owns more than 250 properties across Britain via dozens of companies. They include distribution centres that are key to the flow of food and goods in multiple regions of the UK including the south-west and south-east of England and the Midlands.</p>
      <p>The properties are all ultimately owned via the China Investment Corporation (CIC), which manages the foreign exchange reserves of the People’s Republic of China and is estimated to have more than £970bn of assets.</p>
      <p>Land Registry records suggest that CIC has spent at least £580m on UK properties, although the true figure is likely to be significantly higher because some records are incomplete. While CIC was known to be an investor in UK property, the scale and detail of its purchases has remained hidden until now due to the use of a vast array of offshore companies.</p>
      <p>The register, which brought the details to light, indicates that CIC has focused on distribution depots, retail parks and trading estates, including some that are critical to regional infrastructure.</p>
      <p>Chinese investment in the UK has been a source of concern and division within the government. Some MPs welcome the flow of cash into Britain, while others have raised security concerns about the role played by China and Chinese companies in strategic assets.</p>
      <p>In 2020, the government ordered that the telecoms company Huawei be removed from Britain’s 5G mobile phone infrastructure before 2027, a decision that Beijing described as “groundless”. The government also went cold on plans for China General Nuclear to be involved in the building of a new nuclear power plant, buying CGN out of its stake in the planned Sizewell C project.</p>

      <p>The former Conservative leader Iain Duncan Smith said it was concerning that so much of the investment was “disguised” via offshore companies. He drew comparisons with the attempted takeover of the UK tech company Newport Wafer Fab, which initially appeared to be the target of a Dutch company before its ultimate Chinese ownership was revealed. The government eventually blocked the deal on security grounds.</p>
      <p>“You sometimes have to go back several links to discover who actually owns the company,” said Duncan Smith, who chairs the international Inter-Parliamentary Alliance on China.</p>
